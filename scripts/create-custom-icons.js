#!/usr/bin/env node

const fs = require('fs');
const path = require('path');

// Create a simple SVG icon and convert to PNG using Node.js
function createSVGIcon(size) {
  return `
<svg width="${size}" height="${size}" viewBox="0 0 ${size} ${size}" xmlns="http://www.w3.org/2000/svg">
  <defs>
    <linearGradient id="grad1" x1="0%" y1="0%" x2="100%" y2="100%">
      <stop offset="0%" style="stop-color:#667eea;stop-opacity:1" />
      <stop offset="100%" style="stop-color:#764ba2;stop-opacity:1" />
    </linearGradient>
    <linearGradient id="grad2" x1="0%" y1="0%" x2="100%" y2="100%">
      <stop offset="0%" style="stop-color:#4facfe;stop-opacity:1" />
      <stop offset="100%" style="stop-color:#00f2fe;stop-opacity:1" />
    </linearGradient>
  </defs>
  
  <!-- Background circle -->
  <circle cx="${size/2}" cy="${size/2}" r="${size/2 - 2}" fill="url(#grad1)" />
  
  <!-- Inner circle -->
  <circle cx="${size/2}" cy="${size/2}" r="${size/3}" fill="url(#grad2)" opacity="0.8" />
  
  <!-- Star icon -->
  <g transform="translate(${size/2}, ${size/2})">
    <path d="M0,-${size/4} L${size/12},-${size/12} L${size/4},0 L${size/12},${size/12} L0,${size/4} L-${size/12},${size/12} L-${size/4},0 L-${size/12},-${size/12} Z" 
          fill="white" opacity="0.9" />
  </g>
  
  <!-- Small accent dots -->
  <circle cx="${size * 0.75}" cy="${size * 0.25}" r="${size/16}" fill="white" opacity="0.6" />
  <circle cx="${size * 0.25}" cy="${size * 0.75}" r="${size/16}" fill="white" opacity="0.6" />
</svg>
  `.trim();
}

// Create icons directory if it doesn't exist
const iconsDir = path.join(__dirname, '..', 'public', 'icons');
if (!fs.existsSync(iconsDir)) {
  fs.mkdirSync(iconsDir, { recursive: true });
}

// Generate SVG icons for different sizes
const sizes = [16, 32, 48, 128];

console.log('üé® Creating beautiful custom icons for Chrome extension...');

sizes.forEach(size => {
  const svgContent = createSVGIcon(size);
  const svgPath = path.join(iconsDir, `icon${size}.svg`);
  
  fs.writeFileSync(svgPath, svgContent);
  console.log(`‚úÖ Created icon${size}.svg (${size}x${size})`);
});

console.log('\nüéâ Custom SVG icons created successfully!');
console.log('\nüìù Note: For production, consider converting SVG to PNG using:');
console.log('   - Online tools like CloudConvert');
console.log('   - Command line tools like ImageMagick');
console.log('   - Or use the SVG files directly in manifest.json');

// Create a README for the icons
const readmePath = path.join(iconsDir, 'README.md');
const readmeContent = `# Chrome Extension Icons

This directory contains the icons for the HeadHunter Resume Auto-Boost Chrome extension.

## Icon Sizes

- \`icon16.svg\` - 16x16px - Used in the extension toolbar
- \`icon32.svg\` - 32x32px - Used in the extension management page
- \`icon48.svg\` - 48x48px - Used in the extension management page
- \`icon128.svg\` - 128x128px - Used in the Chrome Web Store

## Design

The icons feature a modern gradient design with:
- Primary gradient: Purple to blue (#667eea to #764ba2)
- Secondary gradient: Light blue to cyan (#4facfe to #00f2fe)
- Star symbol representing "boost" functionality
- Clean, professional appearance

## Usage

These SVG icons can be used directly in the manifest.json or converted to PNG format for broader compatibility.

Generated by: scripts/create-custom-icons.js
`;

fs.writeFileSync(readmePath, readmeContent);
console.log('üìö Created README.md with icon documentation'); 